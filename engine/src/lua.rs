impl mlua::UserData for LuaWorld {
    fn add_fields<F: mlua::UserDataFields<Self>>(_fields: &mut F) {}

    fn add_methods<M: mlua::UserDataMethods<Self>>(methods: &mut M) {
        methods.add_method(
            "spawn",
            |lua: &mlua::Lua, world: &Self, ()| -> mlua::Result<mlua::AnyUserData> {
                let ecs_world = unsafe { &mut *world.world };
                let entity = ecs_world.spawn();
                lua.create_userdata(entity)
            },
        );
        methods.add_method(
            "get_resource",
            |lua, world, name: String| -> mlua::Result<mlua::Value> {
                let ecs_world = unsafe { &mut *world.world };
                let reg = world.type_registry.registrations.get(&name).ok_or(
                    mlua::Error::RuntimeError(format!("not registered: {}", name)),
                )?;
                let ptr = ecs_world.get_mut_resource_dyn(&reg.typeid).ok_or(
                    mlua::Error::RuntimeError(format!("resource not found: {}", name)),
                )?;
                let raw_ptr = ptr.as_mut() as *mut dyn std::any::Any as *mut u8;
                (reg.lua_from_ptr)(raw_ptr, lua)
            },
        );
        methods.add_method(
            "query",
            |lua: &mlua::Lua, world: &Self, query: mlua::Table| -> mlua::Result<mlua::Value> {
                let ecs_world = unsafe { &mut *world.world };

                let mut req_typeids = Vec::new();
                let req_names: Vec<String> = query
                    .get::<mlua::Table>("req")
                    .unwrap_or(lua.create_table()?)
                    .sequence_values::<String>()
                    .collect::<mlua::Result<_>>()?;
                for name in &req_names {
                    let reg = world.type_registry.registrations.get(name).ok_or(
                        mlua::Error::RuntimeError(format!("type not registered: {}", name)),
                    )?;
                    req_typeids.push(reg.typeid);
                }

                let mut opt_typeids = Vec::new();
                let opt_names: Vec<String> = query
                    .get::<mlua::Table>("opt")
                    .unwrap_or(lua.create_table()?)
                    .sequence_values::<String>()
                    .collect::<mlua::Result<_>>()?;
                for name in &opt_names {
                    let reg = world.type_registry.registrations.get(name).ok_or(
                        mlua::Error::RuntimeError(format!("type not registered: {}", name)),
                    )?;
                    opt_typeids.push(reg.typeid);
                }

                let mut not_typeids = Vec::new();
                let not_names: Vec<String> = query
                    .get::<mlua::Table>("not")
                    .unwrap_or(lua.create_table()?)
                    .sequence_values::<String>()
                    .collect::<mlua::Result<_>>()?;
                for name in &not_names {
                    let reg = world.type_registry.registrations.get(name).ok_or(
                        mlua::Error::RuntimeError(format!("type not registered: {}", name)),
                    )?;
                    not_typeids.push(reg.typeid);
                }

                let query_info = QueryInfo {
                    req_typeids,
                    opt_typeids,
                    not_typeids,
                };
                let dyn_query = ecs_world.dyn_query_mut(&query_info);

                let results = lua.create_table()?;
                for (i, (entity, req_ptrs, opt_ptrs)) in dyn_query.iter().enumerate() {
                    let row = lua.create_table()?;
                    row.set("entity", lua.create_userdata(*entity)?)?;

                    // go through each rquired componnet since its returned per entity
                    for (j, name) in req_names.iter().enumerate() {
                        let reg = world.type_registry.registrations.get(name).unwrap();
                        row.set(
                            name.as_str(),
                            (reg.lua_from_ptr)(req_ptrs[j] as *mut u8, lua)?,
                        )?;
                    }

                    for (j, name) in opt_names.iter().enumerate() {
                        match opt_ptrs[j] {
                            Some(ptr) => {
                                let reg = world.type_registry.registrations.get(name).unwrap();
                                row.set(name.as_str(), (reg.lua_from_ptr)(ptr as *mut u8, lua)?)?;
                            }
                            None => row.set(name.as_str(), mlua::Value::Nil)?,
                        }
                    }

                    results.set(i + 1, row)?;
                }

                Ok(mlua::Value::Table(results))
            },
        );
        methods.add_method(
            "get_component",
            |lua: &mlua::Lua,
             world: &Self,
             (entityid, component_name): (mlua::AnyUserData, mlua::String)|
             -> mlua::Result<mlua::Value> {
                let ecs_world = unsafe { &mut *world.world };
                let entity = *entityid.borrow::<EntityId>()?;
                let type_name: &str = &component_name.to_str()?;
                let registry = world.type_registry.registrations.get(type_name).ok_or(
                    mlua::Error::RuntimeError("component type not registered".to_string()),
                )?;
                let type_id = registry.typeid;

                let component_ptr = ecs_world.get_component_dyn(entity, type_id);

                match component_ptr {
                    Some(ptr) => (registry.lua_from_ptr)(ptr, lua),
                    None => Ok(mlua::Value::Nil),
                }
            },
        );
        methods.add_method(
            "add_component",
            |_lua: &mlua::Lua,
            world: &Self,
            (entityid, value, component_name): (mlua::AnyUserData, mlua::Value, mlua::String)|
            -> mlua::Result<()> {
                let ecs_world = unsafe { &mut *world.world };
                let entity = *entityid.borrow::<EntityId>()?;
                let type_name: &str = &component_name.to_str()?;
                let registry = world.type_registry.registrations.get(type_name)
                        .ok_or(mlua::Error::RuntimeError(format!(
                                    "type not registered: {}", type_name
                        )))?;

                let component = if matches!(value, mlua::Value::Nil) {
                    let constructor = registry.default_construct;
                    match constructor {
                        Some(constructor) => {
                            println!("nil passed for {}, constructing default", type_name);
                            constructor()
                        },
                        None => return Err(mlua::Error::RuntimeError(
                                format!("nil passed for {} but it has no default constructor, construct one explicitly", type_name)
                        )),
                    }
                } else {
                    let converter = registry.dyn_from_lua;
                    (converter)(&value)?
                };

                component
                    .add_to_world(ecs_world, entity)
                    .ok()
                    .ok_or(mlua::Error::RuntimeError(format!(
                                "failed to add component to world: entity: {:?}, type: {}",
                                entity, type_name
                    )))?;
                Ok(())
            },
        );
    }
}

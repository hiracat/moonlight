pub struct TypeRegistration {
    pub name: &'static str,
    pub typeid: TypeId,
    pub lua_from_entity: fn(EntityId, *mut World, &mlua::Lua) -> mlua::Result<mlua::Value>,
    pub lua_from_resource: fn(*mut World, &mlua::Lua) -> mlua::Result<mlua::Value>,
    pub dyn_from_lua: fn(&mlua::Value, &mut World) -> mlua::Result<Box<dyn DynamicComponent>>,
    pub default_construct: Option<fn() -> Box<dyn DynamicComponent>>,
}

pub struct TypeRegistry {
    pub registrations: HashMap<String, &'static TypeRegistration>,
}


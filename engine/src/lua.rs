impl<T: LuaSeralize + Clone> LuaSeralize for Vec<T> {
    fn from_lua(value: &mlua::Value, world: &mut World) -> mlua::Result<Self> {
        match value {
            mlua::Value::Table(t) => {
                let mut result = Vec::new();
                for i in 1.. {
                    match t.get::<mlua::Value>(i)? {
                        mlua::Value::Nil => break,
                        v => result.push(T::from_lua(&v, world)?),
                    }
                }
                Ok(result)
            }
            _ => Err(mlua::Error::RuntimeError(format!(
                "expected table for Vec, found {}",
                value.type_name()
            ))),
        }
    }
    fn to_lua(&self, lua: &mlua::Lua, world: &mut World) -> mlua::Result<mlua::Value> {
        let table = lua.create_table()?;
        for (i, item) in self.iter().enumerate() {
            table.set(i + 1, item.to_lua(lua, world)?)?;
        }
        Ok(mlua::Value::Table(table))
    }
}

impl<T: LuaSeralize> LuaSeralize for Option<T> {
    fn from_lua(value: &mlua::Value, world: &mut World) -> mlua::Result<Self> {
        match value {
            mlua::Value::Nil => Ok(None),
            v => Ok(Some(T::from_lua(v, world)?)),
        }
    }
    fn to_lua(&self, lua: &mlua::Lua, world: &mut World) -> mlua::Result<mlua::Value> {
        match self {
            Some(v) => v.to_lua(lua, world),
            None => Ok(mlua::Value::Nil),
        }
    }
}
impl<T: LuaSeralize> LuaSeralize for Box<T> {
    fn from_lua(value: &mlua::Value, world: &mut World) -> mlua::Result<Self> {
        Ok(Box::new(T::from_lua(value, world)?))
    }
    fn to_lua(&self, lua: &mlua::Lua, world: &mut World) -> mlua::Result<mlua::Value> {
        (**self).to_lua(lua, world)
    }
}

impl<K, V> LuaSeralize for HashMap<K, V>
where
    K: LuaSeralize + Eq + std::hash::Hash,
    V: LuaSeralize,
{
    fn to_lua(&self, lua: &mlua::Lua, world: &mut World) -> mlua::Result<mlua::Value> {
        let table = lua.create_table()?;

        for (k, v) in self.iter() {
            let key = k.to_lua(lua, world)?;
            let value = v.to_lua(lua, world)?;

            table.set(key, value)?;
        }

        Ok(mlua::Value::Table(table))
    }

    fn from_lua(value: &mlua::Value, world: &mut World) -> mlua::Result<Self> {
        match value {
            mlua::Value::Table(table) => {
                let mut map = HashMap::new();

                for pair in table.pairs::<mlua::Value, mlua::Value>() {
                    let (k, v) = pair?;

                    let key = K::from_lua(&k, world)?;
                    let value = V::from_lua(&v, world)?;

                    map.insert(key, value);
                }

                Ok(map)
            }
            other => Err(mlua::Error::FromLuaConversionError {
                from: other.type_name(),
                to: "HashMap".to_string(),
                message: Some("expected Lua table".into()),
            }),
        }
    }
}

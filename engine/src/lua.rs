pub struct TypeRegistration {
    pub name: &'static str,
    pub typeid: TypeId,
    pub lua_from_ptr: LuaFromPtrFn,
    // readback without type information is only ever used to add components to the world
    pub dyn_from_lua: DynComponentFromLuaFn,
    pub default_construct: Option<DefualtConstructFn>,
}

pub struct TypeRegistry {
    pub registrations: HashMap<String, &'static TypeRegistration>,
}
pub struct LuaTypeDoc {
    pub name: &'static str,
    pub fields: &'static [(&'static str, &'static str)],
}
inventory::collect!(TypeRegistration);
inventory::collect!(LuaTypeDoc);

impl TypeRegistry {
    fn new() -> Self {
        let mut registrations = HashMap::new();
        for item in inventory::iter::<TypeRegistration>() {
            registrations.insert(item.name.to_string(), item);
        }
        TypeRegistry { registrations }
    }
}


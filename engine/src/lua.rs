impl mlua::UserData for LuaWorld {
    fn add_fields<F: mlua::UserDataFields<Self>>(_fields: &mut F) {}

    fn add_methods<M: mlua::UserDataMethods<Self>>(methods: &mut M) {
        methods.add_method(
            "spawn",
            |lua: &mlua::Lua, world: &Self, name: String| -> mlua::Result<mlua::AnyUserData> {
                let ecs_world = unsafe { &mut *world.world };
                let entity = ecs_world.spawn(&name);
                lua.create_userdata(entity)
            },
        );
        methods.add_method("despawn", |_lua, world, id: AnyUserData| {
            let entity = *id.borrow::<EntityId>()?;
            let ecs_world = unsafe { &mut *world.world };
            ecs_world
                .despawn(entity)
                .map_err(|e| mlua::Error::RuntimeError(format!("{:?}", e)))?;
            Ok(())
        });
        methods.add_method(
            "get_resource",
            |lua, world, name: String| -> mlua::Result<mlua::Value> {
                let reg = world
                    .type_registry
                    .registrations
                    .get(&name)
                    .ok_or_else(|| {
                        mlua::Error::RuntimeError(format!("not registered: {}", name))
                    })?;
                (reg.lua_from_resource)(world.world, lua)
            },
        );
        methods.add_method(
            "query",
            |lua: &mlua::Lua, world: &Self, query: mlua::Table| -> mlua::Result<mlua::Value> {
                let ecs_world = unsafe { &mut *world.world };

                // helper to resolve a list of type names to registrations + typeids
                let resolve_names = |names: Vec<String>| -> mlua::Result<
                    Vec<(String, &'static TypeRegistration, TypeId)>,
                > {
                    names
                        .into_iter()
                        .map(|name| {
                            let reg =
                                world
                                    .type_registry
                                    .registrations
                                    .get(&name)
                                    .ok_or_else(|| {
                                        mlua::Error::RuntimeError(format!(
                                            "type not registered: {}",
                                            name
                                        ))
                                    })?;
                            let typeid = reg.typeid;
                            Ok((name, *reg, typeid))
                        })
                        .collect()
                };

                let req = resolve_names(
                    query
                        .get::<mlua::Table>("req")
                        .unwrap_or(lua.create_table()?)
                        .sequence_values::<String>()
                        .collect::<mlua::Result<_>>()?,
                )?;

                let opt = resolve_names(
                    query
                        .get::<mlua::Table>("opt")
                        .unwrap_or(lua.create_table()?)
                        .sequence_values::<String>()
                        .collect::<mlua::Result<_>>()?,
                )?;

                let not = resolve_names(
                    query
                        .get::<mlua::Table>("not")
                        .unwrap_or(lua.create_table()?)
                        .sequence_values::<String>()
                        .collect::<mlua::Result<_>>()?,
                )?;

                let query_info = QueryInfo {
                    req_typeids: req.iter().map(|(_, _, id)| *id).collect(),
                    opt_typeids: opt.iter().map(|(_, _, id)| *id).collect(),
                    not_typeids: not.iter().map(|(_, _, id)| *id).collect(),
                };

                let dyn_query = ecs_world.dyn_query_mut(&query_info);
                let results = lua.create_table()?;

                for (i, (entity, _req_ptrs, _opt_ptrs)) in dyn_query.iter().enumerate() {
                    let row = lua.create_table()?;
                    row.set("entity", lua.create_userdata(*entity)?)?;

                    for (name, reg, _) in &req {
                        row.set(
                            name.as_str(),
                            (reg.lua_from_entity)(*entity, world.world, lua)?,
                        )?;
                    }

                    for (name, reg, _) in &opt {
                        let value = (reg.lua_from_entity)(*entity, world.world, lua)
                            .ok()
                            .unwrap_or(mlua::Value::Nil);
                        row.set(name.as_str(), value)?;
                    }

                    results.set(i + 1, row)?;
                }

                Ok(mlua::Value::Table(results))
            },
        );
        methods.add_method(
            "get_component",
            |lua: &mlua::Lua,
             world: &Self,
             (entityid, component_name): (mlua::AnyUserData, mlua::String)|
             -> mlua::Result<mlua::Value> {
                let entity = *entityid.borrow::<EntityId>()?;
                let type_name: &str = &component_name.to_str()?;
                let registry = world.type_registry.registrations.get(type_name).ok_or(
                    mlua::Error::RuntimeError("component type not registered".to_string()),
                )?;

                (registry.lua_from_entity)(entity, world.world, lua).map_err(|x| {
                    mlua::Error::RuntimeError(format!("get component failed with {:?}", x))
                })
            },
        );
        methods.add_method(
            "find",
            |lua: &mlua::Lua, world: &Self, name: String| -> mlua::Result<mlua::Value> {
                let ecs_world = unsafe { &mut *world.world };
                match ecs_world.find(&name) {
                    Some(entity) => Ok(mlua::Value::UserData(lua.create_userdata(entity)?)),
                    None => Ok(mlua::Value::Nil),
                }
            },
        );
        methods.add_method(
            "add_component",
            |_lua: &mlua::Lua,
            world: &Self,
            (entityid, value, component_name): (mlua::AnyUserData, mlua::Value, mlua::String)|
            -> mlua::Result<()> {
                let ecs_world = unsafe { &mut *world.world };
                let entity = *entityid.borrow::<EntityId>()?;
                let type_name: &str = &component_name.to_str()?;
                let registry = world.type_registry.registrations.get(type_name)
                        .ok_or(mlua::Error::RuntimeError(format!(
                                    "type not registered: {}", type_name
                        )))?;

                let component = if matches!(value, mlua::Value::Nil) {
                    let constructor = registry.default_construct;
                    match constructor {
                        Some(constructor) => {
                            println!("nil passed for {}, constructing default", type_name);
                            constructor()
                        },
                        None => return Err(mlua::Error::RuntimeError(
                                format!("nil passed for {} but it has no default constructor, construct one explicitly", type_name)
                        )),
                    }
                } else {
                    let converter = registry.dyn_from_lua;
                    (converter)(&value, ecs_world)?
                };

                component
                    .add_to_world(ecs_world, entity)
                    .ok()
                    .ok_or(mlua::Error::RuntimeError(format!(
                                "failed to add component to world: entity: {:?}, type: {}",
                                entity, type_name
                    )))?;
                Ok(())
            },
        );
        methods.add_method(
            "remove_component",
            |_lua: &mlua::Lua,
             world: &Self,
             (entityid, component_name): (mlua::AnyUserData, mlua::String)|
             -> mlua::Result<()> {
                let ecs_world = unsafe { &mut *world.world };
                let entity = *entityid.borrow::<EntityId>()?;
                let type_name: &str = &component_name.to_str()?;
                let registry = world.type_registry.registrations.get(type_name).ok_or(
                    mlua::Error::RuntimeError(format!("type not registered: {}", type_name)),
                )?;

                ecs_world
                    .remove_dyn(entity, registry.typeid)
                    .map_err(|err| {
                        mlua::Error::RuntimeError(format!(
                            "failed to add component to world: entity: {:?}, type: {} with err {:?}",
                            entity, type_name, err
                        ))
                    })?;
                Ok(())
            },
        );
    }
}

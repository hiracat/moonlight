pub trait PrintOnError {
    fn print_on_error(self);
}

impl<T, E: std::fmt::Debug> PrintOnError for Result<T, E> {
    fn print_on_error(self) {
        if let Err(e) = self {
            eprintln!("Error: {:?}", e);
        }
    }
}

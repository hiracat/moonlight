pub trait LuaSeralize
where
    Self: Sized,
{
    fn from_lua(value: &mlua::Value, world: &mut World) -> mlua::Result<Self>;
    fn to_lua(&self, lua: &mlua::Lua, world: &mut World) -> mlua::Result<mlua::Value>;
}


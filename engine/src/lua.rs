impl LuaSeralize for f32 {
    fn from_lua(value: &mlua::Value) -> mlua::Result<Self> {
        match value {
            mlua::Value::Number(n) => Ok(*n as f32),
            mlua::Value::Integer(n) => Ok(*n as f32),
            _ => Err(mlua::Error::RuntimeError(format!(
                "expected number for f32, found {}",
                value.type_name()
            ))),
        }
    }
    fn to_lua(&mut self, _lua: &mlua::Lua) -> mlua::Result<mlua::Value> {
        Ok(mlua::Value::Number(*self as f64))
    }
}
impl LuaSeralize for uv::Vec3 {
    fn from_lua(value: &mlua::Value) -> mlua::Result<Self> {
        let table = value.as_table().ok_or_else(|| {
            mlua::Error::RuntimeError(format!(
                "number field was of the wrong type: {}",
                value.type_name()
            ))
        })?;

        Ok(uv::Vec3::new(
            table.get("x")?,
            table.get("y")?,
            table.get("z")?,
        ))
    }

    fn to_lua(&mut self, lua: &mlua::Lua) -> mlua::Result<mlua::Value> {
        let table = lua.create_table()?;
        table.set("x", self.x)?;
        table.set("y", self.y)?;
        table.set("z", self.z)?;
        Ok(mlua::Value::Table(table))
    }
}


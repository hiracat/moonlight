#[derive(Clone)]
struct Rotor3Lua {
    roll: f32,
    pitch: f32,
    yaw: f32,
}

impl ::mlua::UserData for Rotor3Lua {
    fn add_fields<F: ::mlua::UserDataFields<Self>>(fields: &mut F) {
        fields.add_field_method_get("roll", |_, this| Ok(this.roll));
        fields.add_field_method_set("roll", |_, this, val: f32| {
            this.roll = val;
            Ok(())
        });
        fields.add_field_method_get("pitch", |_, this| Ok(this.pitch));
        fields.add_field_method_set("pitch", |_, this, val: f32| {
            this.pitch = val;
            Ok(())
        });
        fields.add_field_method_get("yaw", |_, this| Ok(this.yaw));
        fields.add_field_method_set("yaw", |_, this, val: f32| {
            this.yaw = val;
            Ok(())
        });
    }
}

impl LuaSeralize for uv::Rotor3 {
    fn from_lua(value: &mlua::Value, _world: &mut World) -> mlua::Result<Self> {
        let (roll, pitch, yaw) = match value {
            ::mlua::Value::UserData(ud) => {
                let r = ud.borrow::<Rotor3Lua>()?;
                (r.roll, r.pitch, r.yaw)
            }
            ::mlua::Value::Table(t) => (t.get("roll")?, t.get("pitch")?, t.get("yaw")?),
            _ => {
                return Err(::mlua::Error::RuntimeError(format!(
                    "expected Rotor3 userdata or table, found {}",
                    value.type_name()
                )));
            }
        };
        Ok(uv::Rotor3::from_euler_angles(roll, pitch, yaw))
    }
    fn to_lua(&self, lua: &::mlua::Lua, _world: &mut World) -> ::mlua::Result<::mlua::Value> {
        let mat = self.into_matrix();
        let pitch = mat.cols[2].y.asin();
        let yaw = (-mat.cols[2].x).atan2(mat.cols[2].z);
        let roll = (-mat.cols[0].y).atan2(mat.cols[1].y);
        let ud = lua.create_userdata(Rotor3Lua { roll, pitch, yaw })?;
        Ok(::mlua::Value::UserData(ud))
    }
}

//WARNING: this is duplicated in the proc-macros crate, updates should also go there
fn rust_type_to_lua(rust_type: &str) -> String {
    let split_idx = rust_type
        .find(|c: char| !c.is_alphanumeric() && c != '_')
        .unwrap_or(rust_type.len());

    let (base_type, suffix) = rust_type.split_at(split_idx);

    let lua_type = match base_type {
        "f32" | "f64" | "i32" | "i64" | "u32" | "u64" | "isize" | "usize" => "number",
        "String" => "string",
        "bool" => "boolean",
        other => other,
    };

    format!("{lua_type}{suffix}")
}

pub fn write_lua_docs(path: &str) {
    let mut output = String::new();
    output.push_str("---@meta\n");
    output.push_str("-- This file is auto-generated by the engine. Do not edit.\n\n");

    for reg in inventory::iter::<LuaTypeDoc>() {
        output.push_str(&format!("---@class {}\n", reg.name));
        for (field_name, field_type) in reg.fields {
            output.push_str(&format!(
                "---@field {} {}\n",
                field_name,
                rust_type_to_lua(field_type)
            ));
        }
        output.push('\n');
    }

    for reg in inventory::iter::<LuaUnionDoc>() {
        for (variant_name, data_type) in reg.variants {
            let class_name = format!("{}_{}", reg.name, variant_name);
            output.push_str(&format!("---@class {}\n", class_name));
            output.push_str(&format!("---@field type \"{}\"\n", variant_name));
            if !data_type.is_empty() {
                output.push_str(&format!("---@field data {}\n", rust_type_to_lua(data_type)));
            }
            output.push('\n');
        }

        let union_str = reg
            .variants
            .iter()
            .map(|(v, _)| format!("{}_{}", reg.name, v))
            .collect::<Vec<_>>()
            .join(" | ");
        output.push_str(&format!("---@alias {} {}\n\n", reg.name, union_str));
    }

    std::fs::write(path, output).expect("failed to write lua docs");
}

impl LuaVM {
    pub fn new(path: &str) -> LuaVM {
        write_lua_docs("data/scripts/typedef.lua");
        let lua = mlua::Lua::new();
        let src = std::fs::read_to_string(path).unwrap();
        let err = lua.load(src).exec();
        err.print_on_error();
        dbg!(&path);
        Self {
            lua,
            path: path.to_string(),
            type_registry: Arc::new(TypeRegistry::new()),
            last_modified: std::time::SystemTime::now(),
        }
    }

    pub fn run_script(
        &mut self,
        world: &mut World,
        engine: &mut Engine,
        entry_point: &str,
    ) -> Result<(), mlua::Error> {
        self.maybe_reload()?;
        let function: mlua::Function = self.lua.globals().get(entry_point)?;
        // make the lua user data
        let lua_world_ud = self.lua.create_userdata(LuaWorld {
            world: world as *mut World,
            query_active: false,
            type_registry: self.type_registry.clone(),
        })?;
        let lua_engine_ud = self.lua.create_userdata(LuaEngine {
            engine: engine as *mut Engine,
        })?;
        function.call::<()>((lua_world_ud, lua_engine_ud))?;
        Ok(())
    }

    pub fn maybe_reload(&mut self) -> Result<(), mlua::Error> {
        let modified = std::fs::metadata(&self.path)?.modified()?;
        if modified > self.last_modified {
            let src = std::fs::read_to_string(&self.path)?;
            self.lua.load(src).exec()?;
            self.last_modified = modified;
        }

        Ok(())
    }
}


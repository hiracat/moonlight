pub trait LuaSeralize
where
    Self: Sized,
{
    fn from_lua(value: &mlua::Value) -> mlua::Result<Self>;
    fn to_lua(&mut self, lua: &mlua::Lua) -> mlua::Result<mlua::Value>;
}

type LuaFromPtrFn = fn(*mut u8, &mlua::Lua) -> mlua::Result<mlua::Value>;
type DynComponentFromLuaFn = fn(&mlua::Value) -> mlua::Result<Box<dyn DynamicComponent>>;
type DefualtConstructFn = fn() -> Box<dyn DynamicComponent>;
